#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*x;
	size_t	i;

	x = (char *)s;
	i = 0;
	while (i < n)
	{
		x[i] = c;
		i++;
	}
	return ((void *)x);
}

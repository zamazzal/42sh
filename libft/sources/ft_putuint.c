#include "libft.h"

void		ft_putuint(t_ullint n)
{
	char	a;

	if (n < 10)
	{
		a = n + 48;
		write(1, &a, 1);
	}
	else
	{
		ft_putuint(n / 10);
		a = n % 10 + '0';
		write(1, &a, 1);
	}
}

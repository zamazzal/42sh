#include "libft.h"

char		*ft_tabtostr(char **tab)
{
	int		i;
	char	*str;

	i = 0;
	str = ft_strdup("");
	while (tab[i])
	{
		if (i && tab[i + 1])
			str = ft_jandf(str, " ", 1, 0);
		str = ft_jandf(str, tab[i], 1, 0);
		i++;
	}
	dfre(tab);
	return (str);
}
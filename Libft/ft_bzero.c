#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char		*str;
	size_t				i;

	if (!s)
		return ;
	i = 0;
	str = (unsigned char *)s;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
}
